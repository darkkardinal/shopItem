
package shopitem;


public class Cards {
    
    private int cardId;
    private int cardPercent;

    public Cards(int cardId, int cardPercent) {
        this.cardId = cardId;
        this.cardPercent = cardPercent;
    }

    public int getCardId() {
        return cardId;
    }

    public int getCardPercent() {
        return cardPercent;
    }
  
    
}
